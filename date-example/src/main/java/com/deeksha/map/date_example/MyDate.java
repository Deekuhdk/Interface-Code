package com.deeksha.map.date_example;

public class MyDate {
 private int dd,mm,yyyy;

 public MyDate() {
	 
 }
public MyDate(int dd, int mm, int yyyy) {
	
	this.dd = dd;
	this.mm = mm;
	this.yyyy = yyyy;
}

public int getDd() {
	return dd;
}

public int getMm() {
	return mm;
}

public int getYyyy() {
	return yyyy;
}


 
}
